    //funcion llenar tabla
    void consultar(){
    String sql="select * from producto";
   
    
    try{
        
        conet=con1.getConnection();
        st= conet.createStatement();
        rs= st.executeQuery(sql);
        Object[]producto = new Object[7];
   
        modelo=(DefaultTableModel) Tabla.getModel();
        while(rs.next()){   
        producto[0]=rs.getInt("id");
        producto[1]=rs.getString("nombre");
        producto[2]=rs.getString("precio");
        producto[3]=rs.getString("cantidad");
        producto[4]=rs.getString("marca");
        producto[5]=rs.getInt("precio")*rs.getInt("cantidad");
        
        suma=suma+rs.getInt("precio")*rs.getInt("cantidad");
        
   
        producto[6]=rs.getString("fechamodificacion");  
        
        modelo.addRow(producto);
        

        }
        
        Tabla.setModel(modelo);
        
    }catch(Exception e){
    }//fin try 
    
   
    txtgrantotal.setText(String.valueOf(suma));

    }
